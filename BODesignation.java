class BODesignation {
	String name, salary, work_profile;
	int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSalary() {
		return salary;
	}

	public void setSalary(String salary) {
		this.salary = salary;
	}

	public String getWork_profile() {
		return work_profile;
	}

	public void setWork_profile(String work_profile) {
		this.work_profile = work_profile;
	}

	public String toString () {
		return name;
	}
}